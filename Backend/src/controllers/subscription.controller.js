// controller to return subscriber list of a channel
const getUserChannelSubscribers = asyncHandler(async (req, res) => {
    const {channelId} = req.params
    const Channel = await Subscription.findById({channel : channelId})
    const channel = await User.aggregate([
        {
            $lookup: {
                from: "subscriptions",
                localField: "_id",
                foreignField: "channel",
                as: "subscribers"
            }
        },
        {
        $addFields: {
            subscribersCount: {
                $size: "$subscribers"
            }
        }
        }
    ])
    return res
    .status(200)
    .json(
        new ApiResponse(200, channel[0], "UserchannelSubscribers fetched successfully")
    )
})

